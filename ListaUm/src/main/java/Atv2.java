public class Atv2 {
    public static void main(String[] args) {
        int Valor1 = 40;
        int Valor2 = 10;
        int Maior = Math.max(Valor1, Valor2);

        if (Valor1 == Valor2) {
            System.out.println("Os dois valores são iguais");
        } else {
            System.out.println("Maior valor é: " + Maior);
        }
    }
}
